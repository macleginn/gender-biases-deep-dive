random_effects_full <- as.data.frame(summary_full_mixed$varcor)
model_summary_full <- list(
  fixed_effects = fixed_effects_full,
  random_effects = random_effects_full,
  R2m = rsquared_full_mixed[1, "R2m"],
  R2c = rsquared_full_mixed[1, "R2c"]
)
